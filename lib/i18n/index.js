function readPath(source, path) {
  return String(path || "")
    .split(".")
    .reduce((value, part) => (value && typeof value === "object" ? value[part] : undefined), source);
}

export function getDictionary(locale) {
  const safeLocale = resolveLocale(locale);
  return dictionaries[safeLocale] || dictionaries[DEFAULT_LOCALE];
}

export function translate(locale, path, replacements = {}) {
  const safeLocale = resolveLocale(locale);
  const template =
    readPath(dictionaries[safeLocale], path) ??
    readPath(dictionaries[DEFAULT_LOCALE], path) ??
    path;

  return Object.entries(replacements).reduce(
    (value, [key, replacement]) =>
      String(value).replaceAll(`{${key}}`, String(replacement ?? "")),
    String(template)
  );
}

export function createTranslator(locale) {
  const safeLocale = resolveLocale(locale);
  return (path, replacements) => translate(safeLocale, path, replacements);
}

export function formatDate(value, locale, options = {}) {
  return new Intl.DateTimeFormat(getIntlLocale(locale), options).format(new Date(value));
}

export function formatDateTime(value, locale, options = {}) {
  return new Intl.DateTimeFormat(getIntlLocale(locale), options).format(new Date(value));
}

